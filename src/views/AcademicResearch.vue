    <h2 id="self-grav">Self-Gravitating Protostellar Discs</h2>

    <p>My main thesis topic, self-gravitating protostellar discs are a common outcome of the star formation process.  During the collapse of giant molecular clouds into protostars, the angular momentum in the collapsing material forms a disc with a mass that rivals that of the newly formed protostar.</p>
    <p>I explored the rapid evolution of these discs, as their mass flowed onto the protostar.  We were interested in whether the disc could become unstable and break up into planetary mass bodies.</p>

    <p>Through numerical hydrodynamic simulations, synthetic imaging and population synthesis, my work showed that this process could form very massive giant planets and brown dwarfs at great separation from the protostar (i.e. beyond the orbit of Pluto in today's Solar System).</p>


    <h2 id="climate">Extrasolar Planetary Climates</h2>

    <p>As the extrasolar planet detections began to stack up with the Kepler Space Telescope, I started to get interested in the habitability of these worlds (or potentially their moons).</p>  
    <p>I spent a good deal of time building 1D Energy Balance Models, that modelled the latitudinal flow of heat from stellar radiation across the planet's atmosphere.  
      We applied it in various places, including to planets orbiting binary stars (such as the Alpha Centauri system) and moons of giant planets. We discovered some pretty strange results, like the direction of a moon's orbit directly affecting its climate (through the frequency of eclipses by the parent planet).</p>

    <h2 id="seti">The Search For Extraterrestrial Intelligence (SETI)</h2>

    <p>From the beginning of my academic career, I was doodling ideas about how our understanding of planet formation could improve our estimates for the number of technological civilisations in the Milky Way.</p>
    <p>Those ideas grew into Monte Carlo Realisation models of how the Galaxy could produce large or small numbers of civilisations, and how those galaxies might look to radio telescopes if we were living there.  I tried out different solutions to Fermi's Paradox, and asked "what would that mean for humans trying to find alien intelligence?"</p>
    <p>Over the years, I got involved in lots of other projects thanks to collaboration with the Breakthrough Initiatives.  These included drafting new post-detection protocols, establishing new scales for evaluating candidate signals and much more.</p>
